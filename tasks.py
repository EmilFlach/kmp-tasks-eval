"""
KMP tasks eval — task definitions based on real kotlinconf-app commits.

Each Task represents one git commit from https://github.com/JetBrains/kotlinconf-app.

Verification strategy
---------------------
Rather than regex heuristics, the verify function is generated by
`_make_commit_verifier`.  It:
  1. Reads the list of files changed by the commit from `git diff --name-status`.
  2. For each changed file, fetches the expected final content via `git show`.
  3. Compares the agent's version of that file against the expected content using
     a line-level SequenceMatcher ratio.
  4. Passes when enough files reach the similarity threshold.

A second, independent gate in the evaluator runs the JVM Gradle compilation to
confirm the code actually builds.
"""

from __future__ import annotations

import difflib
import re
import subprocess
import tomllib
from dataclasses import dataclass
from pathlib import Path
from typing import Callable, Optional

_TOML_PATH = Path(__file__).parent / "tasks.toml"

KOTLINCONF_REPO_URL = "https://github.com/JetBrains/kotlinconf-app"
KOTLINCONF_BUILD_TASK = ":app:shared:compileKotlinJvm"


# ---------------------------------------------------------------------------
# Result types
# ---------------------------------------------------------------------------

@dataclass
class VerificationResult:
    passed: bool
    score: float            # 0.0 – 1.0
    checks: list[tuple[bool, str]]

    @property
    def summary(self) -> str:
        n_pass = sum(1 for p, _ in self.checks if p)
        status = "PASS" if self.passed else "FAIL"
        return f"{status} ({n_pass}/{len(self.checks)} checks)"

    @property
    def details(self) -> list[str]:
        return [f"{'✓' if p else '✗'} {desc}" for p, desc in self.checks]


@dataclass
class Task:
    id: str
    name: str
    category: str
    parent_sha: str     # Agent starts here
    commit_sha: str     # Agent must reproduce this commit
    prompt: str
    verify: Callable[[Path, Path], VerificationResult]   # (project_dir, repo)
    run_build: bool = True    # Whether to run JVM compile after the agent runs
    setup: Optional[Callable[[Path], None]] = None
    repo_url: str = KOTLINCONF_REPO_URL   # Which git repo to clone/extract from
    build_task: str = KOTLINCONF_BUILD_TASK  # Gradle task for JVM compilation check


# ---------------------------------------------------------------------------
# Git helpers (used inside verify closures)
# ---------------------------------------------------------------------------

def _git_diff_status(repo: Path, parent_sha: str, commit_sha: str) -> list[tuple[str, str]]:
    """Return [(status, path), ...] for files changed between parent and commit.
    Status is one of A (added), M (modified), D (deleted), R (renamed), etc.
    """
    result = subprocess.run(
        ["git", "diff", "--name-status", parent_sha, commit_sha],
        cwd=str(repo),
        capture_output=True,
        text=True,
        check=True,
    )
    entries: list[tuple[str, str]] = []
    for line in result.stdout.strip().splitlines():
        if "\t" in line:
            parts = line.split("\t")
            status = parts[0]
            # Renamed files: "R100\told_path\tnew_path" — take the new path
            path = parts[-1]
            entries.append((status, path))
    return entries


def _git_show(repo: Path, sha: str, path: str) -> str:
    """Return the file content at the given commit, or '' if not found."""
    result = subprocess.run(
        ["git", "show", f"{sha}:{path}"],
        cwd=str(repo),
        capture_output=True,
        text=True,
    )
    return result.stdout if result.returncode == 0 else ""


def _similarity(actual: str, expected: str) -> float:
    """Line-level SequenceMatcher ratio, ignoring blank lines and leading whitespace."""
    def norm(text: str) -> list[str]:
        return [ln.strip() for ln in text.splitlines() if ln.strip()]
    return difflib.SequenceMatcher(None, norm(actual), norm(expected)).ratio()


def _read(path: Path) -> str:
    try:
        return path.read_text(encoding="utf-8")
    except (FileNotFoundError, IsADirectoryError):
        return ""


# ---------------------------------------------------------------------------
# Verifier factory
# ---------------------------------------------------------------------------

def _make_commit_verifier(
    commit_sha: str,
    parent_sha: str,
    min_similarity: float = 0.70,
    skip_patterns: list[str] | None = None,
) -> Callable[[Path, Path], VerificationResult]:
    """
    Returns a verify(project_dir, sample_repo) function that:
    - Gets the changed files from git diff parent_sha..commit_sha
    - For each file, compares the agent's version against git show commit_sha:<path>
    - Requires min_similarity match on each file
    - Skips files whose path matches any pattern in skip_patterns
    """
    def verify(project: Path, sample_repo: Path) -> VerificationResult:
        try:
            changed = _git_diff_status(sample_repo, parent_sha, commit_sha)
        except subprocess.CalledProcessError as e:
            return VerificationResult(
                passed=False, score=0.0,
                checks=[(False, f"git diff failed: {e}")]
            )

        if skip_patterns:
            changed = [
                (s, p) for s, p in changed
                if not any(re.search(pat, p, re.IGNORECASE) for pat in skip_patterns)
            ]

        if not changed:
            return VerificationResult(
                passed=True, score=1.0,
                checks=[(True, "No files to verify after filtering")]
            )

        checks: list[tuple[bool, str]] = []
        for status, path in changed:
            if status.startswith("D"):
                exists = (project / path).exists()
                checks.append((not exists, f"DELETED {path}"))
            else:
                expected = _git_show(sample_repo, commit_sha, path)
                if not expected:
                    # New binary file or git show failed — skip
                    continue
                actual = _read(project / path)
                ratio = _similarity(actual, expected)
                tag = "[NEW]" if status.startswith("A") else ""
                ok = ratio >= min_similarity
                checks.append((ok, f"{tag} {path}: {ratio:.0%} match (need {min_similarity:.0%})"))

        if not checks:
            return VerificationResult(passed=True, score=1.0, checks=[(True, "No text files to verify")])

        n_pass = sum(1 for ok, _ in checks if ok)
        n = len(checks)
        # Require at least 70% of files to pass
        threshold = max(1, round(n * 0.70))
        return VerificationResult(
            passed=n_pass >= threshold,
            score=n_pass / n,
            checks=checks,
        )

    return verify


# ---------------------------------------------------------------------------
# Task registry — loaded from tasks.toml
# ---------------------------------------------------------------------------

def _load_tasks() -> list[Task]:
    with open(_TOML_PATH, "rb") as f:
        data = tomllib.load(f)
    tasks = []
    for d in data["tasks"]:
        tasks.append(Task(
            id=d["id"],
            name=d["name"],
            category=d["category"],
            parent_sha=d["parent_sha"],
            commit_sha=d["commit_sha"],
            prompt=d["prompt"].strip(),
            verify=_make_commit_verifier(
                commit_sha=d["commit_sha"],
                parent_sha=d["parent_sha"],
                min_similarity=d.get("min_similarity", 0.70),
                skip_patterns=d.get("skip_patterns") or None,
            ),
            run_build=d.get("run_build", True),
            repo_url=d.get("repo_url", KOTLINCONF_REPO_URL),
            build_task=d.get("build_task", KOTLINCONF_BUILD_TASK),
        ))
    return tasks


TASKS: list[Task] = _load_tasks()
TASKS_BY_ID: dict[str, Task] = {t.id: t for t in TASKS}
